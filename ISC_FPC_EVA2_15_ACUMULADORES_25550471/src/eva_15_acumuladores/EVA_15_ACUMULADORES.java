public class EVA_15_ACUMULADORES {
    
    public static void main(String[] args) {
        //ACUMULADOR: 
        //++ -- += -= *= /=
        int cuenta = 0;
        //ASIGNACION --> =
        System.out.println(cuenta);
        cuenta = cuenta + 100;//acumulador
        System.out.println(cuenta);
        cuenta = cuenta + 1000;
        System.out.println(cuenta);
        cuenta = cuenta - 800;// cuenta -=800
        System.out.println(cuenta);
        cuenta = cuenta + 2000;
        System.out.println(cuenta);
        
        
        
    }
    
}
